/*********************************************************************
*
*       _WriteGear
*
* Function description
*   Show current gear
*/
static void _WriteGear(int Gear) {
  int xSize;
  int ySize;

  if (_ShowGear) {
    if (_OldGear != Gear) {
      xSize = LCD_GetXSize();
      ySize = LCD_GetYSize();
      _OldGear = Gear;
      GUI_SetColor(GUI_WHITE);
      GUI_SetTextMode(GUI_TM_NORMAL);
      GUI_SetFont(FONT_GEAR);
      GUI_DispDecAt(Gear, ((xSize + GUI_GetStringDistX(STR_GEAR) + 1) / 2) - GUI_GetCharDistX('1'), (ySize - GUI_GetFontDistY()) / 2, 1);
    }
  }
}

/*********************************************************************
*
*       _GetAngle_0
*/
static float _GetAngle_0(int tDiff) {
  // Gear 1
  if ((tDiff >= 0)    && (tDiff <  1000)) {                return 225 - 15 - 0.075f  *  tDiff;         }
  if ((tDiff >= 1000) && (tDiff <  1250)) {                return 225 - 90 + 0.3f    * (tDiff - 1000); }
  // Gear 2
  if ((tDiff >= 1250) && (tDiff <  3250)) { _WriteGear(2); return 225 - 15 - 0.0375f * (tDiff - 1250); }
  if ((tDiff >= 3250) && (tDiff <  3500)) {                return 225 - 90 + 0.3f    * (tDiff - 3250); }
  // Gear 3
  if ((tDiff >= 3500) && (tDiff <  6000)) { _WriteGear(3); return 225 - 15 - 0.03f   * (tDiff - 3500); }
  if ((tDiff >= 6000) && (tDiff <  6250)) {                return 225 - 90 + 0.3f    * (tDiff - 6000); }
  // Gear 4
  if ((tDiff >= 6250) && (tDiff <  9250)) { _WriteGear(4); return 225 - 15 - 0.025f  * (tDiff - 6250); }
  if ((tDiff >= 9250) && (tDiff <  9500)) {                return 225 - 90 + 0.3f    * (tDiff - 9250); }
  // Gear 5
  if ((tDiff >= 9500) && (tDiff < 15000)) { _WriteGear(5); return 225 - 15 - 0.0035f * (tDiff - 9500); }
  return 225 - 90;
}

/*********************************************************************
*
*       _GetAngle_1
*/
static float _GetAngle_1(int tDiff) {
  // Gear 1
  if ((tDiff >=    0) && (tDiff <  1000)) { return 225 -      0.02f   *  tDiff;         }
  if ((tDiff >= 1000) && (tDiff <  1250)) { return 225 - 20;                            }
  // Gear 2
  if ((tDiff >= 1250) && (tDiff <  3250)) { return 225 - 20 - 0.01f   * (tDiff - 1250); }
  if ((tDiff >= 3250) && (tDiff <  3500)) { return 225 - 40;                            }
  // Gear 3
  if ((tDiff >= 3500) && (tDiff <  6000)) { return 225 - 40 - 0.004f  * (tDiff - 3500); }
  if ((tDiff >= 6000) && (tDiff <  6250)) { return 225 - 50;                            }
  // Gear 4
  if ((tDiff >= 6250) && (tDiff <  9250)) { return 225 - 50 - 0.004f  * (tDiff - 6250); }
  if ((tDiff >= 9250) && (tDiff <  9500)) { return 225 - 62;                            }
  // Gear 5
  if ((tDiff >= 9500) && (tDiff < 15000)) { return 225 - 62 - 0.0012f * (tDiff - 9500); }
  return 225 - 90;
}

